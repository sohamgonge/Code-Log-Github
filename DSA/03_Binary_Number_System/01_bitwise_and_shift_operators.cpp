#include <iostream>
using namespace std;

int main()
{
    cout << "Bit wise Operations : " << endl;
    cout << "6 & 10 : " << (6 & 10) << endl;
    cout << "6 | 10 : " << (6 | 10) << endl;
    cout << "6 ^ 10 : " << (6 ^ 10) << endl;
    cout << endl;
    cout << "Leftshift and Rightshift : " << endl;
    cout << "10 << 2 : " << (10 << 2) << endl;
    cout << "10 >> 1 : " << (10 >> 1) << endl;
    return 0;
}